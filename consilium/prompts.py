"""
Промпты раундов и судьи.

Инвариант: system-prompt участника / судьи идёт отдельно и идентичен во всех вызовах
дискуссии → prompt caching работает. User-сообщение несёт контекст раунда (тему,
предыдущие раунды, инструкцию).
"""
from __future__ import annotations

from consilium.models import ParticipantConfig

JUDGE_OUTPUT_SCHEMA_INSTRUCTION = """\
Ответь СТРОГО по этой схеме (markdown с точными заголовками первого уровня):

# TL;DR
<3–5 строк: суть консенсуса и главных разногласий>

# Точки консенсуса
- <пункт>
- <пункт>

# Точки разногласия
- <разногласие с указанием, кто с какой стороны и сильнейшим аргументом каждой>

# Уникальный вклад каждого участника
- <role_slug>: <что именно невоспроизводимо — что пропустили бы другие, одна-две фразы>
- <role_slug>: <…>

# Слепые зоны консилиума
- <что все, похоже, упустили — от тебя>

# Рекомендованное решение
<итоговый рекомендуемый ход>

# Оценка вклада
- <role_slug>: <0-3>
- <role_slug>: <0-3>

Где: 0 — повторил других, 1 — мелкое уточнение, 2 — существенный новый угол,
3 — ключевая идея. Будь строг: 3 ставится редко.
"""


def build_round_user_message(
    *,
    topic: str,
    round_index: int,
    transcript_so_far: str,
    participant: ParticipantConfig,  # noqa: ARG001 — part of public contract, may be used in future rounds
    total_rounds: int,  # noqa: ARG001 — reserved for round-count-aware prompting
) -> str:
    """User-сообщение для конкретного раунда.

    Участник играет роль из своей `ParticipantConfig.system_prompt` (передаётся отдельно
    как system). Здесь — только контекст раунда.
    """
    if round_index == 0:
        return (
            f"ТЕМА ДИСКУССИИ:\n{topic}\n\n"
            f"Это Раунд 0 — Заявления. Ответь с чистого листа, не видя ответов других "
            f"участников. Зафиксируй исходную позицию из своей роли. Будь конкретен, "
            f"СТРОГО 500–700 слов, не больше."
        )

    if round_index == 1:
        return (
            f"ТЕМА ДИСКУССИИ:\n{topic}\n\n"
            f"Ниже — позиции всех участников Раунда 0 (включая твою):\n\n"
            f"{transcript_so_far}\n\n"
            f"Это Раунд 1 — Критика. Найди как минимум одну существенную слабость в "
            f"позиции КАЖДОГО другого участника. Признай слабости своей собственной "
            f"позиции. Не повторяй аргументы из Раунда 0. Разбирай именно позиции "
            f"других участников, а не общие риски. Если с кем-то согласен — кратко "
            f"отметь и иди дальше. СТРОГО 500–700 слов, не больше."
        )

    return (
        f"ТЕМА ДИСКУССИИ:\n{topic}\n\n"
        f"Ниже — полная стенограмма предыдущих раундов:\n\n"
        f"{transcript_so_far}\n\n"
        f"Это Раунд {round_index} — Достройка. С учётом всей критики сформулируй свою "
        f"финальную позицию. Где изменил мнение — честно напиши. Где остался при своём — "
        f"укажи почему критика не убедила. СТРОГО 500–700 слов, не больше."
    )


def build_judge_user_message(*, topic: str, full_transcript: str) -> str:
    return (
        f"ТЕМА ДИСКУССИИ:\n{topic}\n\n"
        f"Полная стенограмма дебатов:\n\n"
        f"{full_transcript}\n\n"
        f"Ты — холодный беспристрастный синтезатор. Ты не участвовал в споре, видишь "
        f"его целиком. Не подстраивайся ни под одну сторону. Будь честен про слабости "
        f"участников.\n\n"
        f"{JUDGE_OUTPUT_SCHEMA_INSTRUCTION}"
    )
